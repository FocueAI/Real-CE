class TP_RRDB(nn.Module):
    """Residual in Residual Dense Block.

    Used in RRDB-Net in ESRGAN.

    Args:
        num_feat (int): Channel number of intermediate features.
        num_grow_ch (int): Channels for each growth.
    """

    def __init__(self, num_feat, num_grow_ch=32, tp_chan_in=64):
        super(TP_RRDB, self).__init__()
        self.rdb1 = ResidualDenseBlock(num_feat, num_grow_ch)
        self.rdb2 = ResidualDenseBlock(num_feat, num_grow_ch)
        self.rdb3 = ResidualDenseBlock(num_feat, num_grow_ch)

        self.cat_conv_1 = nn.Conv2d(tp_chan_in + num_feat, num_feat, 3, 1, 1)
        self.cat_conv_2 = nn.Conv2d(tp_chan_in + num_feat, num_feat, 3, 1, 1)
        self.cat_conv_3 = nn.Conv2d(tp_chan_in + num_feat, num_feat, 3, 1, 1)

    def forward(self, x):
        x, mask = x
        x = torch.cat([x, mask], dim=1)
        x = self.cat_conv_1(x)
        out = self.rdb1(x)
        out = torch.cat([out, mask], dim=1)
        out = self.cat_conv_2(out)
        out = self.rdb2(out)
        out = torch.cat([out, mask], dim=1)
        out = self.cat_conv_3(out)
        out = self.rdb3(out)
        # Emperically, we use 0.2 to scale the residual for better performance
        return [out * 0.2 + x, mask]


